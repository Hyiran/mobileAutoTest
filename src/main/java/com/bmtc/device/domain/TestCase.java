package com.bmtc.device.domain;

import java.io.Serializable;
import java.util.List;

/**
 * @author: Jason.ma
 * @date: 2018年1月9日下午3:06:33
 *
 */
public class TestCase implements Serializable {
	private static final long serialVersionUID = -1L;

	private String testSuite;
	private List<TestCaseTable> caseName;
	
	public String getTestSuite() {
		return testSuite;
	}
	public void setTestSuite(String testSuite) {
		this.testSuite = testSuite;
	}
	public List<TestCaseTable> getCaseName() {
		return caseName;
	}
	public void setCaseName(List<TestCaseTable> caseName) {
		this.caseName = caseName;
	}
	@Override
	public String toString() {
		return "TestCase [testSuite=" + testSuite + ", caseName=" + caseName
				+ "]";
	}
	
}
